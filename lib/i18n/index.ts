export function getLocalizedOptions(category: keyof typeof OPTIONS, lang: "zh" | "en") {
    const options = OPTIONS[category];
    if (lang === "zh") return options;

    return options.map((opt: { label: string; value: string }) => ({
        ...opt,
        label: OPTION_LABELS_EN[opt.value] || opt.label
    }));
}